import fs from 'node:fs';
import path from 'node:path';

const dist = path.resolve('dist');
const indexPath = path.join(dist, 'index.html');
if (!fs.existsSync(indexPath)) {
  console.error('dist/index.html not found. Run npm run build first.');
  process.exit(1);
}

let html = fs.readFileSync(indexPath, 'utf8');
const assetDir = path.join(dist, 'assets');
const cssFiles = fs.existsSync(assetDir) ? fs.readdirSync(assetDir).filter((name) => name.endsWith('.css')) : [];
const jsFiles = fs.existsSync(assetDir) ? fs.readdirSync(assetDir).filter((name) => name.endsWith('.js')) : [];

function escapeForInlineScript(jsText) {
  return jsText
    .replace(/<\/script/gi, '<\\/script')
    .replace(/<!--/g, '<\!--');
}

for (const css of cssFiles) {
  const cssText = fs.readFileSync(path.join(assetDir, css), 'utf8');
  html = html.replace(
    new RegExp(`<link[^>]+${css.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[^>]*>`, 'g'),
    `<style>\n${cssText}\n</style>`
  );
}

for (const js of jsFiles) {
  const jsText = escapeForInlineScript(fs.readFileSync(path.join(assetDir, js), 'utf8'));
  html = html.replace(
    new RegExp(`<script[^>]+${js.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}[^>]*></script>`, 'g'),
    `<script type="module">\n${jsText}\n</script>`
  );
}

const banner = '<!-- Standalone offline build generated by final frontend QA. -->\n';
const standalonePath = path.join(dist, 'standalone.html');
fs.writeFileSync(standalonePath, banner + html);

const launcher = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <meta http-equiv="refresh" content="0; url=./dist/standalone.html" />
    <title>Open Diagnosis Center Offline Preview</title>
    <style>
      body{font-family:Segoe UI,Arial,sans-serif;background:#f0f2f5;color:#202124;display:grid;place-items:center;min-height:100vh;margin:0;padding:24px;}
      .card{max-width:520px;background:#fff;border:1px solid #dadce0;border-radius:18px;padding:28px;box-shadow:0 10px 30px rgba(15,23,42,.12);}
      a{display:inline-block;margin-top:14px;background:#1a73e8;color:#fff;text-decoration:none;padding:12px 18px;border-radius:10px;font-weight:700;}
      p{color:#5f6368;line-height:1.5;}
    </style>
  </head>
  <body>
    <main class="card">
      <h1>Opening offline preview…</h1>
      <p>If it does not open automatically, click the button below.</p>
      <a href="./dist/standalone.html">Open Offline Preview</a>
    </main>
  </body>
</html>
`;
fs.writeFileSync(path.resolve('START_HERE_OFFLINE.html'), launcher);

console.log('Standalone offline preview generated at dist/standalone.html');
console.log('Offline launcher generated at START_HERE_OFFLINE.html');
